    {
        $filter = request('filter');
        if ($filter) {
            $filter = '%' . $filter . '%';
            $query  = Video::query()->where('name', 'like', $filter);
            $videos = $query->paginate(10);
        } else {
            $videos = Video::paginate(10);
        }

        return view('videos', [
            'videos' => $videos,
            'filter' => request('filter')
        ]);
    }

